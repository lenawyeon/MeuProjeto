# Demonstração do uso de operadores. . .
X = int(input("Digite um número: "))
Y = int(input("Digite outro número: "))

SOMA = X + Y
print("resultado da soma:", SOMA)
        
SUBTRACÃO = X - Y
print("resultado da subtração:", SUBTRACÃO)
        
MULTIPLICACÃO = X * Y
print("resultado da multiplicação:", MULTIPLICACÃO)
        
DIVISÃO = X / Y
print("resultado da divisão:", DIVISÃO)

        










        






        
